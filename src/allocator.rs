const START_ADDR: usize = 4;

#[derive(Error, Debug)]
enum Error {
    #[error("Pointer already initialized")]
    AlreadyInitialized,
}

pub struct Memory {
    page: Page,
    snapshot: Snapshot,
    next_addr: usize,
}

impl Memory {
    fn from(page: Page) -> Self {
        Self {
            page,
            snapshot: Snapshot::default(),
            next_addr: START_ADDR,
        }
    }

    pub fn new() -> Self {
        Self {
            page: Page::new(),
            snapshot: Snapshot::default(),
            next_addr: START_ADDR,
        }
    }

    fn alloc(&mut self, size: usize) -> (Addr, &mut [u8]) {
        assert!(size > 0);
        let addr = self.next_addr;
        self.next_addr += size;
        let bytes = self.snapshot.zeroed(addr..addr + size);
        (addr as u32, bytes)
    }

    pub fn read_mut(&mut self, idx: Range<usize>) -> &mut [u8] {
        self.snapshot.zeroed(idx)
    }

    pub fn write(&mut self, addr: Addr, bytes: &[u8]) {
        self.snapshot.write(addr, bytes)
    }

    pub fn read(&self, idx: Range<usize>) -> Cow<[u8]> {
        self.page.as_bytes(idx)
    }

    pub fn read_uncommited(&self, range: Range<usize>) -> Cow<[u8]> {
        self.page.as_bytes_uncommited(range, &self.snapshot)
    }

    pub fn commit(&mut self) {
        let snapshot = mem::take(&mut self.snapshot);
        self.page.commit(snapshot);
    }

    pub fn read_static<const N: usize>(&self, offset: usize) -> [u8; N] {
        let mut ret = [0; N];
        let bytes = self.read_uncommited(offset..offset + N);
        for (to, from) in ret.iter_mut().zip(bytes.into_iter()) {
            *to = *from;
        }
        ret
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn check_simple_allocation() {
        let mut memory = Memory::new();
        let a_ptr = {
            let mut scope = Scope::new(&mut memory);

            let a = scope.create(ListNode {
                value: 42,
                next: Ptr::null(),
            });
            a.ptr()
        };

        let mut scope = Scope::new(&mut memory);
        let handle = scope.lookup(a_ptr);
        assert_eq!(RefCell::borrow(&handle.value).value, 42);
    }

    #[test]
    fn check_complex_allocation() {
        let mut memory = Memory::new();

        let a_ptr = {
            let mut scope = Scope::new(&mut memory);
            let mut b = scope.create(ListNode {
                value: 35,
                next: Ptr::null(),
            });
            let mut a = scope.create(ListNode {
                value: 34,
                next: b.ptr(),
            });
            scope.finish();

            a.ptr()
        };

        let mut scope = Scope::new(&mut memory);
        let list = LinkedList::new(scope, a_ptr);
        assert_eq!(list.len(), 2);
    }
}

#[derive(BinRead, BinWrite)]
struct Ptr<T> {
    addr: u32,
    _phantom: PhantomData<T>,
}

impl<T> Clone for Ptr<T> {
    fn clone(&self) -> Self {
        *self
    }
}

/// Need to implement Copy manually, because derive(Copy) is automatically
/// adding `T: Copy` bound in autogenerated implementation
impl<T> Copy for Ptr<T> {}

impl<T> Ptr<T> {
    fn from_addr(addr: u32) -> Ptr<T> {
        Ptr {
            addr,
            _phantom: PhantomData::<T>,
        }
    }

    fn null() -> Self {
        Self::from_addr(0)
    }

    fn is_null(&self) -> bool {
        self.addr == 0
    }
}

fn borrow_downcast<T: Any>(cell: &RefCell<dyn Any>) -> Option<Ref<T>> {
    let r = cell.borrow();
    if (*r).type_id() == TypeId::of::<T>() {
        Some(Ref::map(r, |x| x.downcast_ref::<T>().unwrap()))
    } else {
        None
    }
}

struct LinkedList<'a> {
    scope: Scope<'a>,
    root: Ptr<ListNode>,
}

#[derive(BinRead, BinWrite, Clone)]
#[brw(little)]
struct ListNode {
    value: i32,
    next: Ptr<ListNode>,
}

impl ServiceEntity for ListNode {
    fn size(&self) -> usize {
        8
    }

    fn write_to(&self, buffer: &mut Cursor<Vec<u8>>) {
        self.write(buffer).unwrap();
    }
}

impl<'a> LinkedList<'a> {
    fn new(scope: Scope<'a>, root: Ptr<ListNode>) -> Self {
        Self { scope, root }
    }

    fn push(&mut self, value: i32) {
        let handle = self.scope.create(ListNode {
            value,
            next: Ptr::null(),
        });
        self.root = handle.ptr();
    }

    fn len(&self) -> usize {
        let mut node: Ptr<_> = self.root;
        let mut len = 0;
        while !node.is_null() {
            // dbg!(node.addr);
            len += 1;
            node = self.scope.lookup(node).as_ref().next;
        }
        len
    }
}

